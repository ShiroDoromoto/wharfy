// AurProbe は AUR RPC で実体の版を引く(04 の実体照合)。
type AurProbe struct {
	Base string // 既定 https://aur.archlinux.org
	HTTP *http.Client
}

// Probe は AUR RPC v5 info で pkgname の版を返す(pkgrel は落とす)。無ければ found=false。
func (p *AurProbe) Probe(ctx context.Context, pkgname string) (RemoteState, error) {
	base := p.Base
	if base == "" {
		base = "https://aur.archlinux.org"
	}
	url := strings.TrimRight(base, "/") + "/rpc/v5/info/" + pkgname
	req, err := http.NewRequestWithContext(ctx, http.MethodGet, url, nil)
	if err != nil {
		return RemoteState{}, err
	}
	cl := p.HTTP
	if cl == nil {
		cl = http.DefaultClient
	}
	resp, err := cl.Do(req)
	if err != nil {
		return RemoteState{}, err
	}
	defer resp.Body.Close()
	body, _ := io.ReadAll(resp.Body)
	if resp.StatusCode != http.StatusOK {
		return RemoteState{}, fmt.Errorf("aur rpc %s: %s", url, resp.Status)
	}
	var out struct {
		Results []struct {
			Version string `json:"Version"`
		} `json:"results"`
	}
	if err := json.Unmarshal(body, &out); err != nil {
		return RemoteState{}, err
	}
	if len(out.Results) == 0 {
		return RemoteState{Found: false}, nil
	}
	return RemoteState{Version: stripPkgrel(out.Results[0].Version), Found: true}, nil
}

// stripPkgrel は AUR の "1.2.3-1" から pkgrel を落として "1.2.3" にする。
func stripPkgrel(v string) string {
	if i := strings.LastIndexByte(v, '-'); i > 0 {
		return v[:i]
	}
	return v
}

// WriteGoReleaser は .wharfy/ に書き、利用者 root には書かない(03)。
func TestWriteGoReleaserNonDestructive(t *testing.T) {
	root := t.TempDir()
	data := []byte("version: 2\n")
	path, err := WriteGoReleaser(root, data)
	if err != nil {
		t.Fatal(err)
	}
	want := filepath.Join(root, ".wharfy", "goreleaser.yaml")
	if path != want {
		t.Errorf("path = %q, want %q", path, want)
	}
	if got, _ := os.ReadFile(path); string(got) != string(data) {
		t.Errorf("written content mismatch")
	}
	// root に .goreleaser.yaml を作っていないこと。
	if _, err := os.Stat(filepath.Join(root, ".goreleaser.yaml")); !os.IsNotExist(err) {
		t.Error("must not write .goreleaser.yaml at repo root")
	}
}

func TestGitignoreNeedsWharfy(t *testing.T) {
	root := t.TempDir()
	if !GitignoreNeedsWharfy(root) {
		t.Error("no .gitignore → should need suggestion")
	}
	os.WriteFile(filepath.Join(root, ".gitignore"), []byte("*.out\nnode_modules/\n"), 0o644)
	if !GitignoreNeedsWharfy(root) {
		t.Error(".wharfy/ absent → should need suggestion")
	}
	os.WriteFile(filepath.Join(root, ".gitignore"), []byte("*.out\n.wharfy/\n"), 0o644)
	if GitignoreNeedsWharfy(root) {
		t.Error(".wharfy/ present → should be idempotent (no suggestion)")
	}
}

// --- helpers ---

func mustGen(t *testing.T, cfg Config, in File) []byte {
	t.Helper()
	data, err := GenerateGoReleaser(cfg, in)
	if err != nil {
		t.Fatal(err)
	}
	return data
}

func toStrings(v any) []string {
	arr, _ := v.([]any)
	out := make([]string, 0, len(arr))
	for _, e := range arr {
		out = append(out, e.(string))
	}
	return out
}

func equalAny(v any, want []string) bool {
	got := toStrings(v)
	if len(got) != len(want) {
		return false
	}
	for i := range got {
		if got[i] != want[i] {
			return false
		}
	}
	return true
}

func assertContains(t *testing.T, hay []string, needle string) {
	t.Helper()
	for _, s := range hay {
		if s == needle {
			return
		}
	}
	t.Errorf("expected %q in %v", needle, hay)
}
